package Strings;

public class StringIsEmptyMethod {

	public static void main(String[] args) {
		
		String str = "";
		if(str.isEmpty())
		{
			System.out.println("String is empty");
		}
		else
		{
			System.out.println("String is not empty");
		}
		
		String str1 = "Abhay";
		if(str1.isEmpty()==true)
		{
			System.out.println("String is empty");
		}
		else
		{
			System.out.println("String is not empty");
		}
	}

}
